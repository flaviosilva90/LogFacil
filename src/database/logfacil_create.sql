/* ── VIEWS DE POSIÇÃO DE ESTOQUE ── */
CREATE VIEW VW_POSICAO_PRODUTOS AS
SELECT P.ID, P.CODIGO, P.DESCRICAO,
       U.SIGLA AS UNIDADE,
       P.ESTOQUE_ATUAL, P.ESTOQUE_MIN, P.ESTOQUE_MAX,
       P.PRECO_CUSTO, P.PRECO_VENDA,
       P.ESTOQUE_ATUAL * P.PRECO_CUSTO AS VL_CUSTO_TOTAL,
       CASE
         WHEN P.ESTOQUE_ATUAL <= 0                                   THEN 'SEM ESTOQUE'
         WHEN P.ESTOQUE_ATUAL < P.ESTOQUE_MIN                        THEN 'ESTOQUE BAIXO'
         WHEN P.ESTOQUE_MAX > 0 AND P.ESTOQUE_ATUAL > P.ESTOQUE_MAX  THEN 'ESTOQUE ALTO'
         ELSE 'NORMAL'
       END AS SITUACAO
FROM PRODUTOS P
LEFT JOIN UNIDADES U ON U.ID = P.ID_UNIDADE
WHERE P.ATIVO = 'S';

CREATE VIEW VW_POSICAO_MP AS
SELECT MP.ID, MP.CODIGO, MP.DESCRICAO,
       U.SIGLA AS UNIDADE,
       MP.ESTOQUE_ATUAL, MP.ESTOQUE_MIN, MP.ESTOQUE_MAX,
       MP.CUSTO_MEDIO,
       MP.ESTOQUE_ATUAL * MP.CUSTO_MEDIO AS VL_TOTAL,
       CASE
         WHEN MP.ESTOQUE_ATUAL <= 0                                    THEN 'SEM ESTOQUE'
         WHEN MP.ESTOQUE_ATUAL < MP.ESTOQUE_MIN                        THEN 'ESTOQUE BAIXO'
         WHEN MP.ESTOQUE_MAX > 0 AND MP.ESTOQUE_ATUAL > MP.ESTOQUE_MAX THEN 'ESTOQUE ALTO'
         ELSE 'NORMAL'
       END AS SITUACAO
FROM MAT_PRIMAS MP
LEFT JOIN UNIDADES U ON U.ID = MP.ID_UNIDADE
WHERE MP.ATIVO = 'S';

