/* ══════════════════════════════════
   TRIGGER – RECEBIMENTO DE COMPRA
   Atualiza estoque MP + custo médio
   ══════════════════════════════════ */
CREATE TRIGGER TAU_COMPRA_RECEBER FOR COMPRAS
  ACTIVE AFTER UPDATE POSITION 0
AS
  DECLARE VARIABLE VID_MP   INTEGER;
  DECLARE VARIABLE VQTD     NUMERIC(15,3);
  DECLARE VARIABLE VVLU     NUMERIC(15,2);
  DECLARE VARIABLE VEST_ANT NUMERIC(15,3);
  DECLARE VARIABLE VCMED    NUMERIC(15,2);
BEGIN
  IF (NEW.STATUS = 'RECEBIDA' AND OLD.STATUS <> 'RECEBIDA') THEN
  BEGIN
    FOR SELECT CI.ID_MAT_PRIMA, CI.QUANTIDADE, CI.VL_UNITARIO
        FROM COMPRAS_ITENS CI WHERE CI.ID_COMPRA = NEW.ID
        INTO :VID_MP, :VQTD, :VVLU
    DO BEGIN
      SELECT ESTOQUE_ATUAL, CUSTO_MEDIO FROM MAT_PRIMAS
        WHERE ID = :VID_MP INTO :VEST_ANT, :VCMED;

      IF ((:VEST_ANT + :VQTD) > 0) THEN
        VCMED = ((:VEST_ANT * :VCMED) + (:VQTD * :VVLU)) / (:VEST_ANT + :VQTD);

      UPDATE MAT_PRIMAS
        SET ESTOQUE_ATUAL = ESTOQUE_ATUAL + :VQTD,
            CUSTO_MEDIO   = :VCMED
        WHERE ID = :VID_MP;

      INSERT INTO ESTOQUE_MP_MOV
        (ID_MAT_PRIMA,ID_USUARIO,TIPO_MOV,ORIGEM,ID_ORIGEM,
         QUANTIDADE,VL_UNITARIO,VL_TOTAL,EST_ANTES,EST_DEPOIS)
      VALUES
        (:VID_MP,NEW.ID_USUARIO,'E','COMPRA',NEW.ID,
         :VQTD,:VVLU,:VQTD*:VVLU,:VEST_ANT,:VEST_ANT+:VQTD);
    END
    UPDATE COMPRAS SET DT_RECEBIMENTO = CURRENT_DATE WHERE ID = NEW.ID;
  END
END^

/* ══════════════════════════════════
   TRIGGER – FATURAMENTO DE VENDA
   Debita estoque de produtos
   ══════════════════════════════════ */
CREATE TRIGGER TAU_VENDA_FATURAR FOR VENDAS
  ACTIVE AFTER UPDATE POSITION 0
AS
  DECLARE VARIABLE VID_PROD INTEGER;
  DECLARE VARIABLE VQTD     NUMERIC(15,3);
  DECLARE VARIABLE VVLU     NUMERIC(15,2);
  DECLARE VARIABLE VEST_ANT NUMERIC(15,3);
BEGIN
  /* Faturamento: debita estoque */
  IF (NEW.STATUS = 'FATURADA' AND OLD.STATUS <> 'FATURADA') THEN
  BEGIN
    FOR SELECT VI.ID_PRODUTO, VI.QUANTIDADE, VI.VL_UNITARIO
        FROM VENDAS_ITENS VI WHERE VI.ID_VENDA = NEW.ID
        INTO :VID_PROD, :VQTD, :VVLU
    DO BEGIN
      SELECT ESTOQUE_ATUAL FROM PRODUTOS
        WHERE ID = :VID_PROD INTO :VEST_ANT;

      UPDATE PRODUTOS SET ESTOQUE_ATUAL = ESTOQUE_ATUAL - :VQTD WHERE ID = :VID_PROD;

      INSERT INTO ESTOQUE_MOV
        (ID_PRODUTO,ID_USUARIO,TIPO_MOV,ORIGEM,ID_ORIGEM,
         QUANTIDADE,VL_UNITARIO,VL_TOTAL,EST_ANTES,EST_DEPOIS)
      VALUES
        (:VID_PROD,NEW.ID_USUARIO,'S','VENDA',NEW.ID,
         :VQTD,:VVLU,:VQTD*:VVLU,:VEST_ANT,:VEST_ANT-:VQTD);
    END
    UPDATE VENDAS SET DT_FATURAMENTO = CURRENT_DATE WHERE ID = NEW.ID;
  END

  /* Cancelamento de venda faturada: estorno automático */
  IF (NEW.STATUS = 'CANCELADA' AND OLD.STATUS = 'FATURADA') THEN
  BEGIN
    FOR SELECT VI.ID_PRODUTO, VI.QUANTIDADE, VI.VL_UNITARIO
        FROM VENDAS_ITENS VI WHERE VI.ID_VENDA = NEW.ID
        INTO :VID_PROD, :VQTD, :VVLU
    DO BEGIN
      SELECT ESTOQUE_ATUAL FROM PRODUTOS
        WHERE ID = :VID_PROD INTO :VEST_ANT;

      UPDATE PRODUTOS SET ESTOQUE_ATUAL = ESTOQUE_ATUAL + :VQTD WHERE ID = :VID_PROD;

      INSERT INTO ESTOQUE_MOV
        (ID_PRODUTO,ID_USUARIO,TIPO_MOV,ORIGEM,ID_ORIGEM,
         QUANTIDADE,VL_UNITARIO,VL_TOTAL,EST_ANTES,EST_DEPOIS,OBS)
      VALUES
        (:VID_PROD,NEW.ID_USUARIO,'E','CANCELAMENTO',NEW.ID,
         :VQTD,:VVLU,:VQTD*:VVLU,:VEST_ANT,:VEST_ANT+:VQTD,
         'Estorno - Cancelamento Venda ' || NEW.NUMERO);
    END
  END
END^

